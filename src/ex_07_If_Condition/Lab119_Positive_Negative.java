package ex_07_If_Condition;

public class Lab119_Positive_Negative {
    public static void main(String[] args) {
        int a = 10;
        if (a > 0) {
            System.out.println("Positive");
        } else if (a < 0) {
            System.out.println("Negative");
        } else {
            System.out.println("Entered Zero");
        }
    }
}
