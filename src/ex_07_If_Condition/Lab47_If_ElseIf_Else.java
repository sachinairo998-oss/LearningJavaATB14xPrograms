package ex_07_If_Condition;

public class Lab47_If_ElseIf_Else {
    public static void main(String[] args) {
        int age = 50;
        if (age > 49) {
            System.out.println("Yes");
        } else if (age < 49) {
            System.out.println("No");
        } else {
            System.out.println("Yes and No");
        }
    }
}
