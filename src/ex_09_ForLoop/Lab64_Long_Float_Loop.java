package ex_09_ForLoop;

public class Lab64_Long_Float_Loop {
    public static void main(String[] args) {
        for (long i = 9876l; i < 10000; i++) { // Long data type valid but not used in loop
            System.out.println(i);
        }
        for (float i = 3.14f; i < 156.78f; i++) { // Float data type valid but not used in loop
            System.out.println(i);
        }
    }
}
