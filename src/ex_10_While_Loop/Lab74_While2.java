package ex_10_While_Loop;

public class Lab74_While2 {
    public static void main(String[] args) {
        int modi = 1; // if 15 then 1 time PM as o/p
        while (modi <= 15) { // 1 to 15 runs will be successfull as <= so 1 to 15
            System.out.println("PM"); // o/p = 15 times PM will be printed
            modi++;
        }
    }
}
